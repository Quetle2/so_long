#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*p;
	size_t	size;

	i = 0;
	size = ft_strlen(str);
	p = malloc(size + 1);
	if (!p)
		return (0);
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
